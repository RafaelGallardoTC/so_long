#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(12, "-2147483648", 1);
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n *= -1;
		}
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar((n % 10) + '0');
	}
}
