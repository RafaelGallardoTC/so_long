#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	char		*d;
	const char	*s;

	d = (char*)dst;
	s = (char*)src;
	while (n-- > 0)
	{
		if ((*d++ = *s++) == (char)c)
			return ((void*)d);
	}
	return (NULL);
}
